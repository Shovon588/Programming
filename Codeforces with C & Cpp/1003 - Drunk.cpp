{
       ll t,i;
       string a,b;
       char ch;
       scanf("%lld",&t);
       for(i=0;i<t;i++){
              scanf("%s%s",&a,&b);
              ch=a.at(0);
              cout << ch << " ";

       }



       return 0;
}
